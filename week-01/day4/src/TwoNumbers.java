public class TwoNumbers {
    public static void main(String[] args) {
        int a = 22;
        int b = 13;

        System.out.println(a + b);
        System.out.println(13 - 22);

        double c = a;
        double d = b;

        System.out.println(c / d);
        System.out.println(a / b);
        System.out.println(a % b);

    }
}
